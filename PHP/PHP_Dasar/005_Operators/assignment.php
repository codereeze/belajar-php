var_dump($number1 += 5);

$number2 = 20;
var_dump($number2 -= 10);

$number3 = 30;
var_dump($number3 *= 15);

$number4 = 40;
var_dump($number4 /= 20);

$number5 = 50;
var_dump($number5 %= 25);

$number6 = 60;
var_dump($number6 **= 30);
