<?php

// if-statement
if (true) {
    echo "Executed if the condition is true" . PHP_EOL;
}


// if-else-statement
if (false) {
    echo 'hello world' . PHP_EOL;
} else {
    echo 'Executed if the condition is false' . PHP_EOL;
}


// else-if-statement
if (false) {
    echo 'hello world' . PHP_EOL;
} else if (true) {
    echo 'Executed if the if-statement is false and this statement condition is true' . PHP_EOL;
} else {
    echo 'world hello' . PHP_EOL;
}
