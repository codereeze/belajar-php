<?php

/**
 * This is assignment operators (<, >, <=, >=, ==, !=, ===, !==)
 *
 */

var_dump(10 < 20); // true
var_dump(10 > 20); // false

var_dump(10 <= 10); // true
var_dump(10 >= 11); // false

var_dump(10 == '10'); // true
var_dump(10 != '10'); // false

var_dump(10 === '10'); // false
var_dump(10 !== '10'); // true