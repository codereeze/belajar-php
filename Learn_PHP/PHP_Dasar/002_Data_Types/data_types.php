<?php
// String data type
var_dump('this is string with single quote');
var_dump("this is string with double quote");

// Integer data type
var_dump(100);
var_dump(1_000_000);

// Float/Double data type
var_dump(3.14);
var_dump(100.1);

// Boolean data type
var_dump(true);
var_dump(false);

// Array
var_dump(array('Apple', 'Melon', 'Orange', 'Banana'));
var_dump(['Apple', 'Melon', 'Orange', 'Banana']);

// Null
var_dump(null);
